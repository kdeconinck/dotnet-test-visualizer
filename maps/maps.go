// Package maps defines functions useful with maps of any type.
package maps

import (
	"cmp"
	"slices"
)

// Keys returns the keys of m (sorted).
func Keys[M ~map[K]V, K cmp.Ordered, V any](m M) []K {
	r := make([]K, 0, len(m))

	for k := range m {
		r = append(r, k)
	}

	slices.Sort(r)

	return r
}
